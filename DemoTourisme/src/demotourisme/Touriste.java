    private void btnModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifierActionPerformed
        Connect();
        String num= numPassport.getText();
        try 
        {
            String query= "UPDATE tourisme SET nom=?, prenom=?, nationalite=?, mois=?, annee=? WHERE numero=?";
            pst= con.prepareStatement(query);
            pst.setString(6,num);
            pst.setString(1, nom.getText());
            pst.setString(2, prenom.getText());
            pst.setString(3,nationalite.getText());
            pst.setString(4, mois.getSelectedItem().toString());
            pst.setString(5,annee.getText());
            
            pst.executeUpdate();
            pst.close();
            con.close();
            vider();
            JOptionPane.showMessageDialog(null, "Touriste modifier");
            tableTouriste();
        } 
        catch (Exception e) 
        {
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_btnModifierActionPerformed

