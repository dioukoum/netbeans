    private void doRecherche(String search)
    {
                try 
        {
            Connect();
            String colonne[]= {"Numero","Nom","Prenom","Nationalité","Mois","Année"};
            DefaultTableModel model= new DefaultTableModel(null,colonne);
            Statement st=con.createStatement();
            rs= st.executeQuery("SELECT * FROM tourisme WHERE nom LIKE '%"+search+"%' OR prenom LIKE '%"+search+"%'");
            while(rs.next())
            {
                String liste[]= {
                    rs.getString("numero"),
                    rs.getString("nom"),
                    rs.getString("prenom"),
                    rs.getString("nationalite"),
                    rs.getString("mois"),
                    rs.getString("annee")
                };
                model.addRow(liste);
            }
            table.setModel(model);
            con.close();
            rs.close();
        } 
        catch (Exception e) 
        {
            e.printStackTrace();
        }
    }
    
    private void tableTouriste()
    {
        try 
        {
            Connect();
            String colonne[]= {"Numero","Nom","Prenom","Nationalité","Mois","Année"};
            DefaultTableModel model= new DefaultTableModel(null,colonne);
            Statement st=con.createStatement();
            rs= st.executeQuery("SELECT * FROM tourisme");
            while(rs.next())
            {
                String liste[]= {
                    rs.getString("numero"),
                    rs.getString("nom"),
                    rs.getString("prenom"),
                    rs.getString("nationalite"),
                    rs.getString("mois"),
                    rs.getString("annee")
                };
                model.addRow(liste);
            }
            table.setModel(model);
            con.close();
            rs.close();
        } 
        catch (Exception e) 
        {
            e.printStackTrace();
        }
    }
    
