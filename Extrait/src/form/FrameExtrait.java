    private void listeExtrait() {
        ExtraitDao edao = new ExtraitDao();
        List<Extrait> le = edao.liste();
        DefaultTableModel model = new javax.swing.table.DefaultTableModel(
                new Object[][]{},
                new String []{
                    "Id","Nom","Prenom","Date naissance","Sexe","Lieu","Nom Pere","Prenom pere","Domicile pere","Nom Mere","Prenom mere","Domicile mere","Date delivrance"
                }
        );
        for (Extrait ex : le) {
            model.addRow(new Object[]{
                ex.getId(),
                ex.getNom(),
                ex.getPrenom(),
                ex.getDatenaiss(),
                ex.getSexe(),
                ex.getLieu(),
                ex.getNompere(),
                ex.getPrenompere(),
                ex.getDomicilepere(),
                ex.getProfessionpere(),
                ex.getNommere(),
                ex.getPrenommere(),
                ex.getDomicilemere(),
                ex.getProfessionmere(),
                ex.getDatedelivrance()
            });
        }
        tbextrait.setModel(model);
        
    }
