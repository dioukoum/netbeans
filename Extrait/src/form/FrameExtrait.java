    private void btnmodifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodifierActionPerformed
        // TODO add your handling code here:
        int index = tbextrait.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(null, "Selectionner un etudiant");
        } else {
            int id = (int) tbextrait.getValueAt(index, 0);
            String nom1 = nom.getText();
            String prenom1 = prenom.getText();
            String sexe1 = sexe.getText();
            String lieu1 = lieu.getText();
            String date1 = datenaiss.getText();
            String nomp = nompere.getText();
            String prenomp = prenompere.getText();
            String domicilep = domicilepere.getText();
            String professionp = professionpere.getText();
            String nomm = nommere.getText();
            String prenomm = prenommere.getText();
            String domicilem = domicilemere.getText();
            String professionm = professionmere.getText();
            String datedeliv = datedelivrance.getText();
            Extrait ex = new Extrait(nom1, prenom1, sexe1, lieu1, nomp, prenomp, domicilep, professionp, nomm, prenomm, domicilem, professionm, date1, datedeliv);
            ExtraitDao edao = new ExtraitDao();
            edao.modifier(ex, id);
            listeExtrait();
        }
    }//GEN-LAST:event_btnmodifierActionPerformed

